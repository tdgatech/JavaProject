public class WhileLoop{
   public static void main(String[]args){
      for(int i = 1; i < 5; i++){
         System.out.println( "The number is "+ i);
      
      
      
      }
      System.out.println();
      int count = 1;
      while(count != 5){
         System.out.println("The number is " + count);
         count++;
      
      
      
      }
      System.out.println();
      count = 1;
      do{
         System.out.println("The number is " + count);
         count++;
      
      }while(count < 5);   
   
   
   }


}