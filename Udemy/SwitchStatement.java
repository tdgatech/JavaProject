public class SwitchStatement{
   public static void main(String[]args){
      int value = 1;
      switch (value){
         case 1: 
            System.out.println("Value was 1");
            break;
         case 2:
            System.out.println("Value was 2");
            break;
         case 3:
            System.out.println("Value was 3");
            break;
         default:
            System.out.println("was not 1,2,3");
            break;
      
      
      }
   
   
   }



}